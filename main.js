/**
 * Hello challenger! Your task is to write a function named `oddOrEven` which takes in a number and returns "odd" if the number is odd and "even" if the number is even.
 *
 * Your implementation should handle numbers only passed into the function).
 *
 * A few examples:
 *    `oddOrEven(3)` should return "odd" as 3 is an odd number
 *    `oddOrEven(4)` should return "even" as 4 is an even number
 */


export function oddOrEven(number) {
    if (number % 2 !== 0) {
        return "odd"
    } return "even"
    }